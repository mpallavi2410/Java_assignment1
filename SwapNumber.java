//Write a program to swap two number. For example a=10 and b=20 output should be a=20 and b=10

package iNeuron_Sept.Assignment1;

public class SwapNumber {

	
	public static void main(String[] args) {
		
	 int a=10;
		 int b=20;
		//before swap
		System.out.println("before swap a is =" +a  + " and b is =" +b);
		
		int temp = a;
		 a=b;
		 b=temp;
		 System.out.println("after swap a is =" +a  + " and b is =" +b);
		 
		 
		
		
		

	}

}
